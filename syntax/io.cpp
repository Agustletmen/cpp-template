//异步 IO（Asynchronous I / O）：发起 IO 操作后立即返回，不阻塞线程，当 IO 操作完成（数据就绪或写入完成）时，由内核通过回调函数、信号或事件通知应用程序处理结果。核心是 “IO 操作在后台完成，完成后通知”。
//Linux：io_uring（高效异步 IO，内核 5.1 + 支持）、aio（早期接口，功能有限）。
//Windows：IOCP（Input Output Completion Port，成熟高效的异步 IO 模型）。
//Boost.Asio、libuv 等跨平台库，统一封装不同系统的异步接口，提供回调或协程接口。


//多路复用（I / O Multiplexing）：通过单线程（或少量线程）监控多个文件描述符（套接字） 的 IO 状态，当某个套接字可读 / 可写时，再进行具体的读写操作。避免了为每个连接创建线程的开销，核心是 “等待事件触发，再处理”。
//select：最早的多路复用接口，支持监控最多 1024 个文件描述符，需轮询检查就绪状态，效率较低。
//poll：改进 select，取消 1024 限制，用链表存储文件描述符，但仍需轮询，适合中小规模连接。
